import sgtk
import os

HookBaseClass = sgtk.get_hook_baseclass()


class PublishVersionPlugin(HookBaseClass):
    @property
    def name(self):
        """
        One line display name describing the plugin
        """
        return "Create a version"

    @property
    def item_filters(self):
        """
        List of item types that this plugin is interested in.

        Only items matching entries in this list will be presented to the
        accept() method. Strings can contain glob patters such as *, for example
        ["maya.*", "file.maya"]
        """
        return ["*"]

    def accept(self, settings, item):
        """
        Method called by the publisher to determine if an item is of any
        interest to this plugin. Only items matching the filters defined via the
        item_filters property will be presented to this method.

        A publish task will be generated for each item accepted here. Returns a
        dictionary with the following booleans:

            - accepted: Indicates if the plugin is interested in this value at
                all. Required.
            - enabled: If True, the plugin will be enabled in the UI, otherwise
                it will be disabled. Optional, True by default.
            - visible: If True, the plugin will be visible in the UI, otherwise
                it will be hidden. Optional, True by default.
            - checked: If True, the plugin will be checked in the UI, otherwise
                it will be unchecked. Optional, True by default.

        :param settings: Dictionary of Settings. The keys are strings, matching
            the keys returned in the settings property. The values are `Setting`
            instances.
        :param item: Item to process

        :returns: dictionary with boolean keys accepted, required and enabled
        """

        return {"accepted": True, "visible": True, "checked": True, "enabled": False}

    def publish(self, settings, item):
        path = item.properties["path"]
        file_name = os.path.basename(path)
        name, extension = os.path.splitext(file_name)
        item.properties["publish_name"] = name
        super(PublishVersionPlugin, self).publish(settings, item)
